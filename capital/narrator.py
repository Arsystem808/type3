
from typing import Literal

Action = Literal["BUY", "SHORT", "CLOSE", "WAIT"]

def trader_tone_narrative_ru(action: Action, horizon: str, last_price: float) -> str:
    """
    Генерирует короткий «голос трейдера» без индикаторов.
    Не раскрывает внутреннюю логику. Только действия и поведение цены.
    """
    base = {
        "intraday": "Действуем аккуратно, ориентируясь на поведение цены здесь и сейчас.",
        "swing": "Рынок сделал шаг и выдохся; фокус на ближайшие 1–3 дня.",
        "position": "Смотрим шире: важны уровни, где цена удерживается неделями."
    }.get(horizon, "")
    
    if action == "BUY":
        return f"Покупатели сохраняют инициативу — берём вход с контролем риска. {base}"
    if action == "SHORT":
        return f"Рынок потерял импульс — ищем отбой для короткой позиции. {base}"
    if action == "CLOSE":
        return f"Движение выработано — фиксируем результат и выходим. {base}"
    return f"Сигнал неочевиден — ждём подтверждения и бережём капитал. {base}"
